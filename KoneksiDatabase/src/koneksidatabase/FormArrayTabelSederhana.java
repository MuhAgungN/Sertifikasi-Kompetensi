    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tb;
        tb = (DefaultTableModel) tblHasil.getModel();
        String kode = tfKode.getText();
        String nama = tfNama.getText();
        String deskripsi = tfDeskripsi.getText();
        tb.setValueAt(kode, selectedRow, 0);
        tb.setValueAt(nama, selectedRow, 1);
        tb.setValueAt(deskripsi, selectedRow, 2);
        DBConnect connect = null;
        try {
            connect = new DBConnect();
        } catch (SQLException ex) {
            Logger.getLogger(FormArrayTabelSederhana.class.getName()).log(Level.SEVERE, null, ex);
        }
        connect.updateBank(nama, deskripsi, tb.getValueAt(selectedRow, 0).toString());
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String kode = tfKode.getText();
        String nama = tfNama.getText();
        String deskripsi = tfDeskripsi.getText();
        tfKode.setText("");
        tfNama.setText("");
        tfDeskripsi.setText("");
        DBConnect connect = null;
        try {
            connect = new DBConnect();
        } catch (SQLException ex) {
            Logger.getLogger(FormArrayTabelSederhana.class.getName()).log(Level.SEVERE, null, ex);
        }
        connect.setDataBank(kode, nama, deskripsi);
        DefaultTableModel model;
        model = (DefaultTableModel) tblHasil.getModel();
        Object[] row;
        row = new Object[]{kode, nama, deskripsi};
        model.addRow(row);
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tb;
        tb = (DefaultTableModel) tblHasil.getModel();
        int kd = Integer.parseInt(tb.getValueAt(selectedRow, 0).toString());

        for (int i = selectedRow; i < index - 1; i++) {
            tb.setValueAt(tb.getValueAt(i + 1, 0), i, 0);
            tb.setValueAt(tb.getValueAt(i + 1, 1), i, 1);
            tb.setValueAt(tb.getValueAt(i + 1, 2), i, 2);
        }
        tb.setValueAt("", selectedRow, 0);
        tb.setValueAt("", selectedRow, 1);
        tb.setValueAt("", selectedRow, 2);
        index--;
        tfKode.setText("");
        tfNama.setText("");
        tfDeskripsi.setText("");
        DBConnect connect = null;
        try {
            connect = new DBConnect();

        } catch (SQLException ex) {
            Logger.getLogger(FormArrayTabelSederhana.class
                    .getName()).log(Level.SEVERE, null, ex);
        }
        connect.deleteBank(kd);
    }//GEN-LAST:event_btnHapusActionPerformed

