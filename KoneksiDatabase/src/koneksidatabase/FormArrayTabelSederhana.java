    class MyList implements ListSelectionListener {

        @Override
        public void valueChanged(ListSelectionEvent e) {
            if (e.getValueIsAdjusting()) {
                return;
            }
            ListSelectionModel lsm = (ListSelectionModel) e.getSource();
            selectedRow = lsm.getMinSelectionIndex();
            TableModel tb = tblHasil.getModel();
            tfKode.setText(tb.getValueAt(selectedRow, 0).toString());
            tfNama.setText(tb.getValueAt(selectedRow, 1).toString());
            tfDeskripsi.setText(tb.getValueAt(selectedRow, 2).toString());
           // tfKode.setEditable(false);
        }
    }

    void pilih() {
        tblHasil.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        ListSelectionModel rowSM = tblHasil.getSelectionModel();
        rowSM.addListSelectionListener(new MyList());

    }

    final void viewDataBank() throws SQLException {
        index = 0;
        DefaultTableModel model;
        model = (DefaultTableModel) tblHasil.getModel();
        DBConnect connect = new DBConnect();
        Object[][] data = connect.getDataBank();
        if (model.getRowCount() == 0) {
            Object[] row;
            for (int i = 0; i < connect.getCountBank(); i++) {
                row = new Object[]{data[i][0], data[i][1], data[i][2]};
                model.addRow(row);
                index++;
            }
        }
    }

